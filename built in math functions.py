#built-in Mathematical functions
#abs -- returns the absolute value
x=-34
y=3.43
print(abs(x),abs(y))

#ord --returns the ASCII value of  the given unicode character
a='f'
b='F'
print(ord(a),ord(b))

#chr --Returns the unicode character for the given ASCII value
c=45
d=38
print(chr(c),chr(d))

#bin --returns the binary string prefixed with '0b' for the given integer
u=55
v=85
print(bin(u),bin(v))
